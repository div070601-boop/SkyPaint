Vec3 MarchingCubes::interpolateEdge(const Vec3& p1, const Vec3& p2,
                                     float v1, float v2) const {
    if (std::abs(v1 - v2) < 1e-6f) return (p1 + p2) * 0.5f;
    float t = (m_isoLevel - v1) / (v2 - v1);
    t = glm::clamp(t, 0.0f, 1.0f);
    return p1 + t * (p2 - p1);
}

Mesh MarchingCubes::extract(const VoxelGrid& grid) const {
    int res = grid.getResolution();
    return extractRegion(grid, 0, 0, 0, res - 2, res - 2, res - 2);
}

Mesh MarchingCubes::extractRegion(const VoxelGrid& grid,
                                   int xMin, int yMin, int zMin,
                                   int xMax, int yMax, int zMax) const {
    Mesh mesh;
    int res = grid.getResolution();

    xMin = std::max(0, xMin);
    yMin = std::max(0, yMin);
    zMin = std::max(0, zMin);
    xMax = std::min(res - 2, xMax);
    yMax = std::min(res - 2, yMax);
    zMax = std::min(res - 2, zMax);

    // Hash map for vertex deduplication
    std::unordered_map<uint64_t, uint32_t> vertexCache;

    for (int z = zMin; z <= zMax; ++z) {
        for (int y = yMin; y <= yMax; ++y) {
            for (int x = xMin; x <= xMax; ++x) {
                // Get the 8 corner values of this cube
                float val[8];
                val[0] = grid.getValue(x, y, z);
                val[1] = grid.getValue(x + 1, y, z);
                val[2] = grid.getValue(x + 1, y + 1, z);
                val[3] = grid.getValue(x, y + 1, z);
                val[4] = grid.getValue(x, y, z + 1);
                val[5] = grid.getValue(x + 1, y, z + 1);
                val[6] = grid.getValue(x + 1, y + 1, z + 1);
                val[7] = grid.getValue(x, y + 1, z + 1);

                // Determine cube index
                int cubeIndex = 0;
                if (val[0] < m_isoLevel) cubeIndex |= 1;
                if (val[1] < m_isoLevel) cubeIndex |= 2;
                if (val[2] < m_isoLevel) cubeIndex |= 4;
                if (val[3] < m_isoLevel) cubeIndex |= 8;
                if (val[4] < m_isoLevel) cubeIndex |= 16;
                if (val[5] < m_isoLevel) cubeIndex |= 32;
                if (val[6] < m_isoLevel) cubeIndex |= 64;
                if (val[7] < m_isoLevel) cubeIndex |= 128;

                if (EDGE_TABLE[cubeIndex] == 0) continue;

                // Get corner positions
                Vec3 pos[8];
                pos[0] = grid.gridToWorld(x, y, z);
                pos[1] = grid.gridToWorld(x + 1, y, z);
                pos[2] = grid.gridToWorld(x + 1, y + 1, z);
                pos[3] = grid.gridToWorld(x, y + 1, z);
                pos[4] = grid.gridToWorld(x, y, z + 1);
                pos[5] = grid.gridToWorld(x + 1, y, z + 1);
                pos[6] = grid.gridToWorld(x + 1, y + 1, z + 1);
                pos[7] = grid.gridToWorld(x, y + 1, z + 1);

                // Interpolate edge vertices
                Vec3 edgeVerts[12];
                if (EDGE_TABLE[cubeIndex] & 1)    edgeVerts[0]  = interpolateEdge(pos[0], pos[1], val[0], val[1]);
                if (EDGE_TABLE[cubeIndex] & 2)    edgeVerts[1]  = interpolateEdge(pos[1], pos[2], val[1], val[2]);
                if (EDGE_TABLE[cubeIndex] & 4)    edgeVerts[2]  = interpolateEdge(pos[2], pos[3], val[2], val[3]);
                if (EDGE_TABLE[cubeIndex] & 8)    edgeVerts[3]  = interpolateEdge(pos[3], pos[0], val[3], val[0]);
                if (EDGE_TABLE[cubeIndex] & 16)   edgeVerts[4]  = interpolateEdge(pos[4], pos[5], val[4], val[5]);
                if (EDGE_TABLE[cubeIndex] & 32)   edgeVerts[5]  = interpolateEdge(pos[5], pos[6], val[5], val[6]);
                if (EDGE_TABLE[cubeIndex] & 64)   edgeVerts[6]  = interpolateEdge(pos[6], pos[7], val[6], val[7]);
                if (EDGE_TABLE[cubeIndex] & 128)  edgeVerts[7]  = interpolateEdge(pos[7], pos[4], val[7], val[4]);
                if (EDGE_TABLE[cubeIndex] & 256)  edgeVerts[8]  = interpolateEdge(pos[0], pos[4], val[0], val[4]);
                if (EDGE_TABLE[cubeIndex] & 512)  edgeVerts[9]  = interpolateEdge(pos[1], pos[5], val[1], val[5]);
                if (EDGE_TABLE[cubeIndex] & 1024) edgeVerts[10] = interpolateEdge(pos[2], pos[6], val[2], val[6]);
                if (EDGE_TABLE[cubeIndex] & 2048) edgeVerts[11] = interpolateEdge(pos[3], pos[7], val[3], val[7]);

                // Generate triangles
                for (int i = 0; TRI_TABLE[cubeIndex][i] != -1; i += 3) {
                    for (int j = 0; j < 3; ++j) {
                        int edgeIdx = TRI_TABLE[cubeIndex][i + j];
                        const Vec3& p = edgeVerts[edgeIdx];

                        // Compute a unique key for the edge being intersected.
                        // We use the grid coordinates of the source voxel and the edge direction.
                        // Edges 0-3 are X-aligned, 4-7 are Y-aligned, 8-11 are Z-aligned.
                        uint64_t vKey = 0;
                        int ex = x, ey = y, ez = z;
                        int dir = 0; // 0=X, 1=Y, 2=Z

                        switch (edgeIdx) {
                            case 0:  dir = 0; break;
                            case 1:  ex++; dir = 1; break;
                            case 2:  ey++; dir = 0; break;
                            case 3:  dir = 1; break;
                            case 4:  ez++; dir = 0; break;
                            case 5:  ex++; ez++; dir = 1; break;
                            case 6:  ey++; ez++; dir = 0; break;
                            case 7:  ez++; dir = 1; break;
                            case 8:  dir = 2; break;
                            case 9:  ex++; dir = 2; break;
                            case 10: ex++; ey++; dir = 2; break;
                            case 11: ey++; dir = 2; break;
                        }

                        // Combine coordinates into a unique 64-bit key
                        vKey = (uint64_t(ex) & 0x1FFFFF) |
                               ((uint64_t(ey) & 0x1FFFFF) << 21) |
                               ((uint64_t(ez) & 0x1FFFFF) << 42) |
                               ((uint64_t(dir) & 0x3) << 62);

                        auto it = vertexCache.find(vKey);
                        if (it != vertexCache.end()) {
                            mesh.indices.push_back(it->second);
                        } else {
                            Vertex vert;
                            vert.position = p;
                            vert.normal = Vec3(0.0f); // computed after
                            vert.uv = Vec2(0.0f);

                            uint32_t newIdx = static_cast<uint32_t>(mesh.vertices.size());
                            mesh.vertices.push_back(vert);
                            mesh.indices.push_back(newIdx);
                            vertexCache[vKey] = newIdx;
                        }
                    }
                }
            }
        }
    }

    mesh.computeNormals();
    return mesh;
}

